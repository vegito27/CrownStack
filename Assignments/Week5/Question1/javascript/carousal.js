function showArrowed(){

	document.querySelector('.automatic').style.display="none"

	document.querySelector('#arrowed2').style.display="block"

	document.querySelector('.arrowed').style.display="block"

	document.querySelector('#dotted2').style.display="none"

	document.querySelector('.dotted').style.display="none"

}

function showDotted(){

	document.querySelector('.automatic').style.display="none"

	document.querySelector('#arrowed2').style.display="none"

	document.querySelector('.arrowed').style.display="none"

	document.querySelector('.dotted').style.display="block"

	document.querySelector('#dotted2').style.display="block"

}

function showAll(){
	
	document.querySelector('.automatic').style.display="block"

	document.querySelector('#arrowed2').style.display="block"

	document.querySelector('.arrowed').style.display="block"

	document.querySelector('#dotted2').style.display="block"

	document.querySelector('.dotted').style.display="block"


}